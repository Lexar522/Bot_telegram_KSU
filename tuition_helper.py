    # –Ø–∫—â–æ –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ —Ç–æ—á–Ω–æ–≥–æ –∑–±—ñ–≥—É, —à—É–∫–∞—î–º–æ —á–∞—Å—Ç–∫–æ–≤—ñ –∑–±—ñ–≥–∏ (–±–µ–∑ word boundary)
    if not specialty_name:
        for name, keywords in specialty_keywords:
            for keyword in keywords:
                if keyword in message_lower:
                    specialty_name = name
                    break
            if specialty_name:
                break
    
    return (specialty_name, specialty_code)

